    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        if (contador == 0) {
            JOptionPane.showMessageDialog(this, "No hay estudiantes registrados aún.");
            return;
        }

        String[] columns = {"ID", "TipoID", "Carnet", "Nombre", "Apellidos", "Nacimiento", "Genero", "Sangre", "Peso", "Altura"};
        Object[][] data = new Object[contador][10];
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");

        for (int i = 0; i < contador; i++) {
            Estudiante e = vecEstudiante[i];
            data[i][0] = e.getID();
            data[i][1] = e.getTipoID();
            data[i][2] = e.getNumeroCarnet();
            data[i][3] = e.getNombre();
            data[i][4] = e.getApellido();
            data[i][5] = e.getNacimiento() != null ? sdf.format(e.getNacimiento()) : "";
            data[i][6] = e.getGenero();
            data[i][7] = e.getTipoSangre();
            data[i][8] = e.getPeso();
            data[i][9] = e.getAltura();
        }

        JTable table = new JTable(data, columns);
        table.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
        JScrollPane scrollPane = new JScrollPane(table);
        scrollPane.setPreferredSize(new Dimension(900, 250));

        JOptionPane.showMessageDialog(this, scrollPane, "Registros de Estudiantes", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_jButton4ActionPerformed

