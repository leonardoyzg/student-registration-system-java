    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(contador<vecEstudiante.length){
            int ID=Integer.parseInt(txtID.getText());
            int tipoID=cboTipoDocumento.getSelectedIndex();
            int Carnet=Integer.parseInt(txtNumeroCarnet.getText());
            String Nombre=txtNombre.getText();
            String Apellidos=txtApellidos.getText();
            Date Nacimiento = null;
            try {
                Nacimiento = new SimpleDateFormat("dd/MM/yyyy").parse(txtNacimiento.getText().trim());
            } catch (ParseException ex) {
                JOptionPane.showMessageDialog(this, "Formato de fecha incorrecto. Use dd/MM/yyyy");
                return;
            }
            String TipoSangre=cboTipoSangre.getSelectedItem().toString();
            char genero=cboGenero.getSelectedItem().toString().charAt(0);
            double Peso=Double.parseDouble(txtPeso.getText());
            double Altura=Double.parseDouble(txtEstatura.getText());
            
            est =new Estudiante(ID, tipoID, Carnet, Nombre, Apellidos, Nacimiento, genero, TipoSangre, Peso, Altura);
            vecEstudiante[contador]=est;
            contador++;
            JOptionPane.showMessageDialog(this, "Se ha ingresado un nuevo estudiante");
            
        } else { JOptionPane.showMessageDialog(this, "No hay espacio para ingresar mas estudiantes");
        
        }
    }//GEN-LAST:event_jButton2ActionPerformed

